import 'package:thingsee_installer/data_classes.dart';
import 'package:flutter/material.dart';

import 'package:thingsee_installer/utilities.dart';
import 'package:thingsee_installer/app_localizations.dart';

class MessagesListView extends StatelessWidget {
  final AutogeneratedMessageData data;

  MessagesListView(this.data);

  Widget _buildMessageItem(BuildContext context, int index) {
    String name = this.data.messages[index].name;
    String timestamp;

    if (name == null || name.isEmpty)
      name = AppLocalizations.of(context).translate('unknown_message');

    DateTime date = new DateTime.fromMillisecondsSinceEpoch(
        this.data.messages[index].tsmTs * 1000);
    if (date != null) timestamp = date.toLocal().toIso8601String();

    return ListTile(
        title: Text(
          name,
          style: new TextStyle(
              fontFamily: 'Haltian Sans',
              fontSize: 18,
              color: HexColor("#000000"),
              fontStyle: FontStyle.normal,
              decoration: TextDecoration.none),
          maxLines: 2,
        ),
        subtitle: Text(
          timestamp,
          style: new TextStyle(
              fontFamily: 'Haltian Sans',
              fontSize: 12,
              color: HexColor("#000000"),
              fontStyle: FontStyle.normal,
              decoration: TextDecoration.none),
          maxLines: 1,
        ));
  }

  @override
  Widget build(BuildContext context) {
    return ListView.builder(
      shrinkWrap: true,
      itemBuilder: _buildMessageItem,
      itemCount: data.messages.length,
      itemExtent: 60,
    );
  }
}
